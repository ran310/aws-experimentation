#!/usr/bin/env node
/**
 * Runs after `cdk synth`. Reads cdk.out and writes aws-infra/generated/*
 * for the local dashboard (stacks-overview.json + architecture.mmd).
 */
import * as fs from 'fs';
import * as path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const cdkOut = path.join(root, 'cdk.out');
const outDir = path.join(root, 'generated');

function main() {
  const manifestPath = path.join(cdkOut, 'manifest.json');
  if (!fs.existsSync(manifestPath)) {
    console.error('generate-infra-artifacts: cdk.out/manifest.json missing. Run cdk synth first.');
    process.exit(1);
  }

  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const stacks = [];

  for (const [id, art] of Object.entries(manifest.artifacts ?? {})) {
    if (art.type !== 'aws:cloudformation:stack') continue;
    const templateFile = art.properties?.templateFile;
    if (!templateFile) continue;

    const templatePath = path.join(cdkOut, templateFile);
    if (!fs.existsSync(templatePath)) continue;

    const template = JSON.parse(fs.readFileSync(templatePath, 'utf8'));
    const resources = template.Resources ?? {};
    const byType = {};
    for (const r of Object.values(resources)) {
      const t = r.Type ?? 'Unknown';
      byType[t] = (byType[t] ?? 0) + 1;
    }

    const outputs = [];
    for (const [key, v] of Object.entries(template.Outputs ?? {})) {
      outputs.push({
        key,
        description: v.Description ?? '',
        exportName: v.Export?.Name ?? null,
      });
    }

    stacks.push({
      id,
      description: template.Description ?? '',
      environment: art.environment ?? '',
      resourceCount: Object.keys(resources).length,
      resourcesByType: byType,
      outputs,
    });
  }

  stacks.sort((a, b) => a.id.localeCompare(b.id));

  const overview = {
    generatedAt: new Date().toISOString(),
    stacks,
  };

  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(
    path.join(outDir, 'stacks-overview.json'),
    JSON.stringify(overview, null, 2) + '\n',
    'utf8',
  );

  const mermaid = buildMermaid(stacks);
  fs.writeFileSync(path.join(outDir, 'architecture.mmd'), mermaid, 'utf8');

  console.log(
    `generate-infra-artifacts: wrote ${stacks.length} stacks to generated/stacks-overview.json and architecture.mmd`,
  );
}

function hasId(stacks, id) {
  return stacks.some((s) => s.id === id);
}

/** True when AwsInfra-Ec2Nginx template includes an internet ALB (e.g. publicAlbHttps). */
function ec2StackHasApplicationLoadBalancer(stacks) {
  const s = stacks.find((x) => x.id === 'AwsInfra-Ec2Nginx');
  if (!s?.resourcesByType) return false;
  const n =
    s.resourcesByType['AWS::ElasticLoadBalancingV2::LoadBalancer'] ?? 0;
  return n > 0;
}

function buildMermaid(stacks) {
  const H = {
    net: hasId(stacks, 'AwsInfra-Network'),
    ec2: hasId(stacks, 'AwsInfra-Ec2Nginx'),
    redis: hasId(stacks, 'AwsInfra-ElastiCacheRedis'),
    api: hasId(stacks, 'AwsInfra-HttpApi'),
    lake: hasId(stacks, 'AwsInfra-Lakehouse-S3'),
  };
  const ec2Alb = H.ec2 && ec2StackHasApplicationLoadBalancer(stacks);

  const parts = [];
  parts.push('%% Auto-generated by bin/generate-infra-artifacts.mjs after cdk synth');
  parts.push('flowchart TB');
  parts.push('  U[Users / Internet]');

  if (H.api) {
    parts.push('  subgraph HTTP["AwsInfra-HttpApi"]');
    parts.push('    APIGW[API Gateway HTTP API]');
    parts.push('    LAM[Lambda Node 20]');
    parts.push('    APIGW --> LAM');
    parts.push('  end');
  }

  if (H.net) {
    parts.push('  subgraph VPC["AwsInfra-Network"]');
    parts.push('    V[VPC 2 AZs]');
    parts.push('    PUB[Public subnets + IGW]');
    parts.push('    PRIV[Private subnets]');
    parts.push('    NAT[NAT Gateway]');
    parts.push('    V --> PUB');
    parts.push('    V --> PRIV');
    parts.push('    PUB --> NAT');
    parts.push('    PRIV --> NAT');
    parts.push('  end');
  }

  if (H.ec2) {
    parts.push('  subgraph EC2S["AwsInfra-Ec2Nginx"]');
    if (ec2Alb) {
      parts.push(
        '    R53["Route 53<br/>DNS for domain"]',
      );
      parts.push(
        '    ACM["ACM<br/>TLS certificate · DNS validation · auto-renewal"]',
      );
      parts.push('    ALB["ALB<br/>HTTPS termination"]');
      parts.push('    EC2[EC2 t4g.nano nginx]');
      parts.push('    R53 -.->|validation records| ACM');
      parts.push('    R53 -.->|A alias| ALB');
      parts.push('    ACM -.->|cert on listener| ALB');
      parts.push('    ALB -->|HTTP :80| EC2');
    } else {
      parts.push('    EC2[EC2 t4g.nano nginx]');
    }
    parts.push('  end');
  }

  if (H.redis) {
    parts.push('  subgraph CACHE["AwsInfra-ElastiCacheRedis"]');
    parts.push('    REDIS[(ElastiCache Redis)]');
    parts.push('  end');
  }

  if (H.lake) {
    parts.push('  subgraph LAKE["AwsInfra-Lakehouse-S3"]');
    parts.push(
      '    LAKEBKT["S3 data lake<br/>prefixes + IAM managed policies"]',
    );
    parts.push('  end');
  }

  if (H.api) parts.push('  U --> APIGW');
  if (H.ec2) {
    parts.push(ec2Alb ? '  U --> ALB' : '  U --> EC2');
  }
  if (H.ec2 && H.net) parts.push('  EC2 --> V');
  if (H.redis && H.net) parts.push('  REDIS --> PRIV');
  if (H.ec2 && H.redis) parts.push('  EC2 --> REDIS');
  if (H.api && H.lake) {
    parts.push('  LAM -.->|attach read/write policy| LAKEBKT');
  }
  if (H.ec2 && H.lake) {
    parts.push('  EC2 -.->|attach policy| LAKEBKT');
  }

  parts.push(
    '  classDef aws fill:#232f3e,color:#fff,stroke:#ff9900,stroke-width:2px',
  );
  const awsNodes =
    'APIGW,LAM,V,PUB,PRIV,NAT,EC2,REDIS' +
    (ec2Alb ? ',R53,ACM,ALB' : '') +
    (H.lake ? ',LAKEBKT' : '');
  parts.push(`  class ${awsNodes} aws`);

  return parts.join('\n') + '\n';
}

main();
